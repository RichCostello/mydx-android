<h3>
  Help - Edit/View Profile</h3>
<p>This view allows you to add a new strain profile, edit an existing one, and view aggregated data for one in the community database.</p>
<p>The Thumbs Up/Thumbs Down button allow you to rate a strain internally to your app.  This is displayed in your history (Search Tab of the My Profile View) for the profiles you have created.</p>
<p>To update or add the strain, simply touch the option on the bottom.  For aggregated results, this is not an option as that view is read-only.</p>

<h3>Feeling Tab</h3>
<p>When adding or editing a profile, this tab allows you to associate feelings and/or medical conditions with the strain.   This allows you to track how this strain makes you feel or what it helps you relieve.  To track this click on one of the three boxes for the given ailment or feeling.  To adjust the medical conditions you wish to track, simply click on the gear box.</p>
<p>When viewing a community profile, this tab displays the aggregated data from all users that have added a community profile based on the strain.</p>
<h3>Content Tab</h3>
<p>This tab displays the stored chemical composition of the strain.  To add data to this, simply select the chemical you have data for and enter it.</p>
<h3>Intake Info</h3>
<p>This tab allows you to add other details about the strain to the profile.  The list boxes allow you to fine tune the profile and select the database you wish to store the profile to.  The community database simply allows this profile to be aggregated with the other community profiles and allows it to come up in the recommend/search results over the community database.</p>

<h3>App Menus</h3>
<p>The hexagon on the bottom right brings up the quick menu and allows you to do a simulated quick test (demo mode) and easily get to your profile.</p>
<p>The rectangular box on top right brings up the main app menu.  This allows you to quickly get to search, forums, education, your profile, settings, and log out.</p>
