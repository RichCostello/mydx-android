<h3>
  Help - Settings
</h3>
<p>The settings view allows you to fine tune this app to set the ailments you wish to track (if applicable) and to setup device and app options.   </p>
<p>The <b>Ailments</b> tab allows you select the ailments you wish to assign to profiles.   To turn on or off an ailment to track, simply click on the control to the right of the ailment name.  You can add ailments by scrolling down, and entering it in the text field at the bottom of the list.</p>
<p>The <b>Usage Settings</b> tab allows you to set app wide options as it pertains to the analyzer.</p>
<h3>App Menus</h3>
<p>The hexagon on the bottom right brings up the quick menu and allows you to do a simulated quick test (demo mode) and easily get to your profile.</p>
<p>The rectangular box on top right brings up the main app menu.  This allows you to quickly get to search, forums, education, your profile, settings, and log out.</p>
