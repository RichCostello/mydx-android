<h3>
  Help - My Profile
</h3>
<p>The My Profile view is your main view for tracking and finding strains.  This view allows you to find strains based on what you would like to relieve and how you would like to feel.  To do a search by ailment or feeling, simply click on the round ball in either the Medical or Recreational tab and touch "RECOMMEND PROFILES" on the bottom.</p>
<p>The <b>Ailments</b> tab shows the ailments you have selected in the settings.  Clicking on one of these balls will allow you to search for profiles based on medical condition.  You can change these by touching the "settings" gear box on top right.</p>
<p>The <b>Side Effects</b> tab shows feelings that you may want to search by.  As with the medical conditions, you can click on one of these and then the "RECOMMEND PROFILES" button to find profiles based on this.</p>
<p>The <b>My Data</b> tab shows all of your profiles you have created and allows you to search in both community and internal databases for strain profiles by name.  You can also add a strain profile from this tab by clicking on the "ADD STRAIN" button on the bottom.</p>
<p>To edit a created profile, simply touch the desired profile.</p>
<h3>Community/Internal Databases</h3>
<p>When recommending by condition/feeling or searching you will be asked which database would you like to search from.</p>
<p>The <b>Community Database</b> are all profiles added by users where they have opted in to share and we have approved.</p>
<p>The <b>Internal Database</b> are the profiles you have added and have marked as such.</b>

<h3>App Menus</h3>
<p>The hexagon on the bottom right brings up the quick menu and allows you to do a simulated quick test (demo mode) and easily get to your profile.</p>
<p>The rectangular box on top right brings up the main app menu.  This allows you to quickly get to search, forums, education, your profile, settings, and log out.</p>
